            <h2>A Brief History</h2>

            <div class="history-container">
                <div class="history-text">
                    <p>In 1789, not long after the U. S. Constitution was ratified, the first major wave of settlers
                        began
                        pouring into the wide-open
                        spaces of western New York in search of farmland. In 1796, one of those settlers staked his
                        claim to
                        a large tract of
                        land north of Seneca Lake and began building the two-story brick farmhouse that would one day be
                        known as the Yorkshire Inn.
                        For a bit of context, George Washington was still president, and Napoleon was on the March in
                        Europe.
                    </p>
                    <p>
                        The original owner, to this day unknown, sold the house and a large portion of his property to a
                        man
                        named Moses Swift in 1824.
                        Legend has it that when his wife saw the home she complained that it was too small, so they
                        built a
                        second home just down the road from the original, and later added another for their children.
                        The two brick homes to the east of the Inn are the original Swift residences.
                    </p>
                    <p>
                        With the advent of regular mail delivery in the early 1800s, stagecoach runs became a common
                        occurrence. This meant, of course, that the coach would need fresh horses, and passengers would
                        require food and lodging. So, a third floor was added to the original farmhouse to provide all
                        the
                        accommodations of a stagecoach stop, and the Yorkshire Inn was born. Evidence of these
                        alterations
                        can still be seen throughout the house today.

                        With a total of six guest suites on the upper floors and a large gathering room on the first,
                        the
                        Inn must have been quite a bustling place. By 1840 there were two mills, two inns, two stores,
                        three
                        shops, and a one-room schoolhouse. Eventually, there would be a small railway station and a post
                        office. The new village was named Unionville.
                    </p>
                    <p>
                        The Erie Canal would pass by seven miles to the north, the Seneca Turnpike seven miles south.
                        Though
                        the towns closest to these new thoroughfares benefitted the most, local farmers were able to
                        bring
                        their goods to bigger markets, assuring greater prosperity for the entire region. Eventually, a
                        railroad line running from Rochester to Auburn was built, putting the nearby town of Phelps,
                        then
                        known as Vienna, on the map. It was during this period that its distinctive cobblestone and cut
                        limestone buildings were built, many still in use today, including the town hall.
                    </p>
                    <p>By the mid-19th century, the road in front of the inn (now Route 96) was a nationally known
                        racecourse for thoroughbreds. The race began at the east end of the town of Vienna (where the
                        Legion
                        Hall is today) and ended right in front of the inn, a full half-mile. Unfortunately, the early
                        railroad tracks crossed the road near each end of the course and the noise of the passing trains
                        disturbed the horses. Commodore Cornelius Vanderbilt personally visited the site to investigate
                        the
                        complaints, and was given free lodging at the Yorkshire Inn. During his stay, he personally
                        experienced the disturbance his trains made to both the horses and the inn's guests. Upon
                        returning
                        to New York City, he ordered the tracks to be moved away from the road to their present
                        location.
                        The races resumed and the inn’s guests slept undisturbed.
                    </p>
                    <p>
                        By the 1940s, the Yorkshire Inn was home to a well-known restaurant, a spot so popular a new
                        addition was built in 1954. It’s safe to say that anyone born before 1980 in this region had at
                        least one meal there. The York Inn restaurant was run successfully until 1976 by James and
                        Vivian
                        Malone, a family still highly regarded in the area to this day.
                    </p>
                    <p>
                        The Latch family purchased the house in 2000 and began converting it into a Bed and Breakfast,
                        opening with a single guest room in 2003, and has been operated as a successful B&B ever since.
                        The
                        next remodel resulted in the addition of The Blue Room and, in 2013, the old restaurant space
                        was
                        converted into the Lodge Suite, the Bolero room, the gathering room, and an additional laundry
                        facility. This is the current state of the Inn today.
                    </p>
                    <p>
                        The Inn was purchased once again by Micah Sherman and Darcy DiPane in 2021 after relocating from
                        Chicago to the Rochester area to be closer to family. Micah brings years of experience in the
                        hospitality industry, having managed one of the top wedding venues in Windy City for over a
                        decade.
                        Darcy is a certified dietician with years of experience in the healthcare industry. Together,
                        they
                        are proud to become a part of the Inn's history and are dedicated to continuing the legacy of
                        their
                        predecessors.
                    </p>
                    <p>Your hosts will provide delicious, home-cooked breakfasts using locally-grown, farm-fresh
                        ingredients, all prepared with a gourmet flair. Each of the four suites has its own distinctive
                        decor and includes a private bath, flat-screen cable TV, and luxurious beds appointed with fine
                        linens. Come join them as they write the next chapter in the ongoing saga of the Yorkshire Inn.
                        They
                        are committed to ensuring your visit will be a memorable one.
                    </p>
